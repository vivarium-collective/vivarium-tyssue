def _floatify(obj):
    """Recursively coerce numpy scalars to plain python floats for clean YAML."""
    if isinstance(obj, dict):
        return {k: _floatify(v) for k, v in obj.items()}
    if isinstance(obj, (list, tuple)):
        return [_floatify(v) for v in obj]
    try:
        import numpy as np

        if isinstance(obj, np.generic):
            return obj.item()
    except Exception:
        pass
    return obj


