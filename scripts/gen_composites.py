INTERVAL = "${interval}"


def tyssue_node(config, interval=INTERVAL):
    return {
        "_type": "process",
        "address": "local:EulerSolver",
        "config": config,
        "inputs": {"behaviors": ["Behaviors"], "global_time": ["global_time"]},
        "outputs": {
            "datasets": ["Datasets"],
            "network_changed": ["Network Changed"],
            "behaviors_update": ["Behaviors"],
        },
        "interval": interval,
    }


def base_stores():
    return {"Network Changed": False, "Behaviors": {}}


def write(name, *, description, tags, processes, state, parameters=None):
    spec = {
        "name": name,
        "description": description,
        "tags": tags,
        "requires": {"processes": processes, "types": ["tyssue_data", "behaviors"]},
        "parameters": parameters or {"interval": {"type": "float", "default": 0.1, "description": "Solver / step interval (dt)."}},
        "state": state,
    }
    path = OUT / f"{name}.composite.yaml"
    with path.open("w") as fh:
        fh.write(f"# Auto-generated by scripts/gen_composites.py — declarative replacement for\n")
        fh.write(f"# the procedural get_test_*_spec() helper in tests/tests.py.\n")
        yaml.safe_dump(spec, fh, sort_keys=False, default_flow_style=False, width=100)
    print(f"wrote {path.relative_to(ROOT)}")


